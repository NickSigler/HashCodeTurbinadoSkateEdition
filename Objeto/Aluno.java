package Objeto;
public class Aluno
{
    private int nota;
    private String nome;
    public Aluno(String nome, int nota)
    {
        this.nome = nome;
        this.nota = nota;
    }
    public String getNome()
    {
        return this.nome;
    }
    public void setNome(String nome)
    {
        this.nome = nome;
    }
    public int getNota()
    {
        return this.nota = nota;
    }
    public void setNota(int nota)
    {
        this.nota = nota;
    }
}
